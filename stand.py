class Solution:
    def __init__(self):
        pass

    def backspaceCompare(self, s: str, t: str) -> bool:
        if (len(s) < 2 or len(s) < 2): return s == t
        if s[-1] == '#':
            if (len(s) > 2 and s[-2] == '#') or len(s) == 1:
                s = s[:-1]
            else:
                s = s[:-2]
        if t[-1] == '#':
            if (len(t) > 2 and t[-2] == '#') or len(t) == 1:
                t = t[:-1]
            else:
                t = t[:-2]
        return self.backspaceCompare(s[:-1], t[:-1]) and s[-1] == t[-1]


